// Fungsi untuk memformat angka
function number_format(number, decimals, dec_point, thousands_sep) {
  number = (number + '').replace(',', '').replace(' ', '');
  var n = !isFinite(+number) ? 0 : +number,
    prec = !isFinite(+decimals) ? 0 : Math.abs(decimals),
    sep = (typeof thousands_sep === 'undefined') ? ',' : thousands_sep,
    dec = (typeof dec_point === 'undefined') ? '.' : dec_point,
    s = '',
    toFixedFix = function(n, prec) {
      var k = Math.pow(10, prec);
      return '' + Math.round(n * k) / k;
    };
  // Fix for IE parseFloat(0.55).toFixed(0) = 0;
  s = (prec ? toFixedFix(n, prec) : '' + Math.round(n)).split('.');
  if (s[0].length > 3) {
    s[0] = s[0].replace(/\B(?=(?:\d{3})+(?!\d))/g, sep);
  }
  if ((s[1] || '').length < prec) {
    s[1] = s[1] || '';
    s[1] += new Array(prec - s[1].length + 1).join('0');
  }
  return s.join(dec);
}

// URL API untuk mendapatkan data pesanan
const apiUrl = "https://pa-test-da05d-default-rtdb.asia-southeast1.firebasedatabase.app/pesanan.json";

// Fungsi untuk memuat data dan memperbarui chart
function loadChartData() {
  $.ajax({
    url: apiUrl,
    method: "GET",
    success: function(data) {
      // Array untuk menyimpan totalHarga dari API
      let monthlyData = Array(12).fill(0); // Inisialisasi dengan 12 bulan, semua bernilai 0

      if (data) {
        // Loop melalui data untuk mengisi totalHarga berdasarkan bulan
        Object.values(data).forEach(pesanan => {
          const bookingDate = new Date(pesanan.booking);
          const monthIndex = bookingDate.getMonth(); // Mendapatkan indeks bulan (0-11)
          monthlyData[monthIndex] += parseFloat(pesanan.totalHarga) || 0;
        });
      }

      // Update dataset chart dengan data baru
      myLineChart.data.datasets[0].data = monthlyData;
      myLineChart.update(); // Memperbarui chart
    },
    error: function(error) {
      console.error("Error fetching data: ", error);
    }
  });
}

